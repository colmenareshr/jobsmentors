def parse_pad_voltage_groups(xlsm_path: pathlib.Path) -> dict[str, str]:
    """Read pad-group voltage settings from XLSM section header rows.

    The Excel macro reads VDDIO_G / VDDIO_J / VDDIO_P from section-header rows
    (col 3 = group name, col 61 = voltage) rather than per-pin io_voltage.
    When a cell contains a compound value like "1.8V/3.3V", the last listed
    voltage is used as the default (matches reference DTSI behaviour).

    Only the three Thor PMC io-pad group names that appear in the DT binding
    are returned: vddio_g, vddio_j, vddio_p.  All other VDDIO_* section
    headers (VDDIO_A, VDDIO_B, VDDIO_C … VDDIO_DDR*) are not PMC pad nodes
    and must not appear in the padvoltage DTSI.
    """
    allowed_groups = {"VDDIO_G", "VDDIO_J", "VDDIO_P"}
    if openpyxl is None:
        raise ImportError("openpyxl is required to parse XLSM files")
    wb = openpyxl.load_workbook(str(xlsm_path), data_only=True, keep_vba=False)
    ws, _ = _detect_data_sheet(wb)
    groups: dict[str, str] = {}
    for row in ws.iter_rows(min_row=1, values_only=True):
        name = _s(row[2])   # col 3
        volt = _s(row[60])  # col 61
        if not name or not volt:
            continue
        name_upper = name.strip().upper()
        if name_upper not in allowed_groups:
            continue
        for opt in reversed(volt.split("/")):
            macro = _VOLTAGE_MACRO.get(opt.strip().lower())
            if macro:
                groups[name_upper.lower()] = macro
                break
    wb.close()
    return groups


def generate_padvoltage_dtsi(pins: list[PinRow], carrier_name: str,
                             xlsm_name: str,
                             pad_voltage_groups: dict[str, str] | None = None
                             ) -> str:
    """Generate the padvoltage DTSI.

    When `pad_voltage_groups` is provided (from `parse_pad_voltage_groups`),
    use those group-level settings - matching how the Excel macro works.
    Falls back to per-pin io_voltage scanning when not provided.
    """
    del carrier_name  # part of the public DTSI-generator signature
    if pad_voltage_groups is not None:
        rail_voltage = pad_voltage_groups
    else:
        rail_voltage = {}
        for pin in pins:
            if not pin.power_rail or not pin.io_voltage:
                continue
            rail = pin.power_rail.lower().strip()
            for opt in reversed(pin.io_voltage.split("/")):
                macro = _VOLTAGE_MACRO.get(opt.strip().lower())
                if macro and rail:
                    rail_voltage[rail] = macro
                    break

    lines: list[str] = []
    lines.append(f"/*This dtsi file was generated by generate_dtsi.py from {xlsm_name} */")
    lines.append(_SPDX_HEADER)
    lines.append("#define IO_PAD_VOLTAGE_1_2V 1200000")
    lines.append("#define IO_PAD_VOLTAGE_1_8V 1800000")
    lines.append("#define IO_PAD_VOLTAGE_3_3V 3300000")
    lines.append("")
    lines.append("")
    lines.append("pmc@8c800000 {")
    lines.append("\tio-pad-defaults {")

    for rail in sorted(rail_voltage):
        lines.append(f"\t\t{rail} {{")
        lines.append(f"\t\t\tnvidia,io-pad-init-voltage = <{rail_voltage[rail]}>;")
        lines.append("\t\t};")
        lines.append("")

    lines.append("\t};")
    lines.append("};")
    lines.append("")
    return "\n".join(lines)


