def _tegra_pin_val(raw: str, enable_val: str = "TEGRA_PIN_ENABLE",
                   disable_val: str = "TEGRA_PIN_DISABLE") -> str:
    """Map 'Enable'/'Disable'/'' → DTS macro string."""
    r = raw.strip().lower()
    if r in ("enable", "1", "yes", "true"):
        return enable_val
    if r in ("disable", "0", "no", "false"):
        return disable_val
    # Default safe fallback
    return disable_val


def _pull_val(pupd: str) -> str:
    r = pupd.strip().lower()
    if "up" in r:
        return "TEGRA_PIN_PULL_UP"
    if "down" in r:
        return "TEGRA_PIN_PULL_DOWN"
    return "TEGRA_PIN_PULL_NONE"


def _drv_type_val(drv_type: str) -> str:
    # AY column values:
    #   "Enable" / "High"     → TEGRA_PIN_2X_DRIVER  (BDGPIO high-drive)
    #   "DEF_1X"              → TEGRA_PIN_DEFAULT_DRIVE_1X  (BDSDMEM pads)
    #   "Disable" / ""        → TEGRA_PIN_1X_DRIVER  (BDGPIO normal)
    r = drv_type.strip().lower()
    if r in ("enable", "1", "high"):
        return "TEGRA_PIN_2X_DRIVER"
    if r in ("def_1x", "default_1x", "default"):
        return "TEGRA_PIN_DEFAULT_DRIVE_1X"
    return "TEGRA_PIN_1X_DRIVER"


def _tristate_for_dir(pin: PinRow) -> str:
    """Tristate is ENABLE for Unused pins or when explicitly set.

    The XLSM tristate column (col 35) carries 'TRISTATE' or 'NORMAL' — map
    those to ENABLE / DISABLE respectively.
    """
    d = pin.pin_direction.strip().lower()
    if d == "unused":
        return "TEGRA_PIN_ENABLE"
    # Use the sheet's tristate column if populated
    ts = pin.tristate.strip().upper()
    if ts == "TRISTATE":
        return "TEGRA_PIN_ENABLE"
    # "NORMAL" → DISABLE (also the fallback for empty)
    return "TEGRA_PIN_DISABLE"


def _einput_for_dir(pin: PinRow) -> str:
    """Enable-input: ENABLE for Input/Bidirectional, DISABLE for Output/Unused."""
    d = pin.pin_direction.strip().lower()
    if d in ("input", "bidirectional", "bidir"):
        return "TEGRA_PIN_ENABLE"
    if pin.e_input:
        return _tegra_pin_val(pin.e_input)
    return "TEGRA_PIN_DISABLE"


