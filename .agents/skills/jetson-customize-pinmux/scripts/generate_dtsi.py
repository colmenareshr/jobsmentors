@dataclass
class _PinIds:
    """Pin identity fields (names, ball, signal)."""
    dt_pin_name: str
    verilog_name: str
    package_ball: str
    connector_pin: str
    signal_name: str
    power_rail: str
    customer_usage: str


@dataclass
class _PinElec:
    """Electrical/configuration fields from the XLSM."""
    pin_direction: str
    gpio_init_val: str
    pupd: str
    tristate: str
    e_input: str
    io_voltage: str
    por_state: str


@dataclass
class _PinDrive:
    """Drive-related electrical fields (open-drain, drive type, loopback)."""
    e_io_od: str
    drv_type: str
    e_lpbk: str


@dataclass
class _PinDerived:
    """Derived fields populated during parsing."""
    sfio_options: list[str] = field(default_factory=list)
    resolved_func: str = ""
    pad_type: str = ""
    pin_group: str = ""


_GROUP_NAMES = ("ids", "elec", "drive", "derived")


@dataclass
class PinRow:
    """One pin's parsed XLSM row, used for DTSI emission + pinmap export."""
    ids: _PinIds
    elec: _PinElec
    drive: _PinDrive
    derived: _PinDerived

    def __getattr__(self, name: str):
        for grp_name in _GROUP_NAMES:
            grp = self.__dict__.get(grp_name)
            if grp is not None and hasattr(grp, name):
                return getattr(grp, name)
        raise AttributeError(name)

    def __setattr__(self, name: str, value) -> None:
        if name in _GROUP_NAMES:
            object.__setattr__(self, name, value)
            return
        for grp_name in _GROUP_NAMES:
            grp = self.__dict__.get(grp_name)
            if grp is not None and hasattr(grp, name):
                setattr(grp, name, value)
                return
        object.__setattr__(self, name, value)


# ---------------------------------------------------------------------------
# XLSM parsing
# ---------------------------------------------------------------------------

def _s(v) -> str:
    """Cell value → stripped string."""
    return "" if v is None else str(v).strip()


def _find_header_row(ws) -> int:
    for r in range(1, min(ws.max_row, 25) + 1):
        if _s(ws.cell(r, 1).value) == "Pin #":
            return r
    return -1


def _detect_data_sheet(wb) -> tuple:
    """Return (worksheet, header_row) for the first data sheet found."""
    for name in wb.sheetnames:
        if name.lower().strip() in _NON_DATA_SHEETS:
            continue
        ws = wb[name]
        hr = _find_header_row(ws)
        if hr != -1:
            return ws, hr
    raise SystemExit("generate_dtsi: no data sheet found in workbook")


def _resolve_func(row_vals: list, cols: dict[str, int]) -> tuple[str, list[str]]:
    """Determine nvidia,function from customer_usage vs mux columns.

    Returns (function_string, sfio_options_list).
    function_string is lowercased DT function name (e.g. 'i2c7_clk', 'gpio').
    """
    cu = _s(row_vals[cols["customer_usage"] - 1]).lower()
    mux_keys = ["mux_gpio", "mux_sfio0", "mux_sfio1", "mux_sfio2", "mux_sfio3"]
    sfio_opts: list[str] = []
    func = ""

    for k in mux_keys:
        raw = _s(row_vals[cols[k] - 1])
        if not raw:
            continue
        lowered = raw.lower()
        # Normalise GPIO entries: "GPIO3_PG.01" → "gpio"
        if re.match(r"gpio\d*_", lowered) or lowered.startswith("gpio"):
            token = "gpio"
        else:
            token = lowered
        if token not in sfio_opts:
            sfio_opts.append(token)
        # Match customer_usage to this mux slot
        if not func:
            cu_norm = re.sub(r"[^a-z0-9]", "_", cu)
            tok_norm = re.sub(r"[^a-z0-9]", "_", token)
            if cu_norm == tok_norm or cu_norm in tok_norm or tok_norm in cu_norm:
                func = token

    # Pins with no mux/sfio options (only mux_unused set) → reserved function.
    if not sfio_opts:
        return "rsvd0", []

    # Fallback: if customer_usage starts with "unused" → rsvd0 (pin not used on
    # this carrier).  Mirrors the Excel VBA macro which outputs rsvd0 for all
    # "unused_*" pins even when the silicon exposes a non-reserved SFIO option.
    if not func:
        if cu.startswith("unused") or cu == "":
            func = "rsvd0"
        else:
            func = re.sub(r"[^a-z0-9_]", "_", cu).strip("_") or "gpio"

    return func, sfio_opts


def _parse_gpio_bank_index(dt_pin_name: str) -> tuple[str, int] | None:
    """Extract (BANK, index) from DT pin name suffix.

    Examples:
      gen7_i2c_scl_pg1  → ("G", 1)
      soc_gpio00_paa0   → ("AA", 0)
      hdmi_cec_paa6     → ("AA", 6)
      soc_gpio61_pal5   → ("AL", 5)
    """
    # Match trailing _p<bank><index> where bank is 1-2 letters
    m = re.search(r"_p([a-z]{1,2})(\d+)$", dt_pin_name.lower())
    if not m:
        return None
    bank = m.group(1).upper()
    idx = int(m.group(2))
    return bank, idx


@dataclass
class _ParsedRowInputs:
    """Already-computed fields passed to _build_pinrow."""
    dt_pin: str
    verilog: str
    ball: str
    pad_type: str
    sfio_opts: list[str]
    func: str


def _build_pinrow(row_vals, cols, parsed: _ParsedRowInputs) -> PinRow:
    """Construct a PinRow from a raw XLSM data row + parsed metadata."""
    return PinRow(
        ids=_PinIds(
            dt_pin_name=parsed.dt_pin,
            verilog_name=parsed.verilog,
            package_ball=parsed.ball,
            connector_pin=_s(row_vals[cols["connector_pin"] - 1]),
            signal_name=_s(row_vals[cols["signal_name"] - 1]),
            power_rail=_s(row_vals[cols["power_rail"] - 1]).lower(),
            customer_usage=_s(row_vals[cols["customer_usage"] - 1]),
        ),
        elec=_PinElec(
            pin_direction=_s(row_vals[cols["pin_direction"] - 1]),
            gpio_init_val=_s(row_vals[cols["gpio_init_val"] - 1]).lower(),
            pupd=_s(row_vals[cols["pupd"] - 1]),
            tristate=_s(row_vals[cols["tristate"] - 1]),
            e_input=_s(row_vals[cols["e_input"] - 1]),
            io_voltage=_s(row_vals[cols["io_voltage"] - 1]),
            por_state=_s(row_vals[cols["por_state"] - 1]),
        ),
        drive=_PinDrive(
            e_io_od=_s(row_vals[cols["e_io_od"] - 1]),
            drv_type=_s(row_vals[cols["drv_type"] - 1]),
            e_lpbk=_s(row_vals[cols["e_lpbk"] - 1]),
        ),
        derived=_PinDerived(
            sfio_options=parsed.sfio_opts,
            resolved_func=parsed.func,
            pad_type=parsed.pad_type,
            pin_group=_s(row_vals[cols["pin_group"] - 1]),
        ),
    )


def _parse_one_row(row_vals: list, cols: dict[str, int]) -> PinRow | None:
    """Process a single raw XLSM data row. Returns None if the row is skipped."""
    ball = _s(row_vals[cols["package_ball"] - 1])
    verilog = _s(row_vals[cols["verilog_name"] - 1])
    if not ball or not verilog:
        return None

    pad_type = _s(row_vals[cols["pad_type"] - 1])
    is_mpio = pad_type.startswith(_MPIO_PAD_PREFIXES)

    has_mux = any(
        _s(row_vals[cols[k] - 1])
        for k in ("mux_gpio", "mux_sfio0", "mux_sfio1", "mux_sfio2", "mux_sfio3")
    )
    has_mux_unused = bool(_s(row_vals[cols["mux_unused"] - 1]))
    if not has_mux and not (is_mpio and has_mux_unused):
        return None

    dt_pin = _s(row_vals[cols["dt_pin_name"] - 1]).lower().replace(" ", "_")
    if not dt_pin:
        dt_pin = verilog.lower().replace(" ", "_")

    func, sfio_opts = _resolve_func(row_vals, cols)
    parsed = _ParsedRowInputs(
        dt_pin=dt_pin, verilog=verilog, ball=ball,
        pad_type=pad_type, sfio_opts=sfio_opts, func=func,
    )
    return _build_pinrow(row_vals, cols, parsed)


def parse_xlsm(xlsm_path: pathlib.Path) -> list[PinRow]:
    """Parse all data rows from the XLSM; return list of PinRow."""
    if openpyxl is None:
        raise ImportError("openpyxl is required to parse XLSM files")
    wb = openpyxl.load_workbook(str(xlsm_path), data_only=True, keep_vba=False)
    ws, header_row = _detect_data_sheet(wb)
    cols = THOR_COLS
    max_col = max(cols.values()) + 2

    rows: list[PinRow] = []
    for r in range(header_row + 1, ws.max_row + 1):
        row_vals = [ws.cell(r, c).value for c in range(1, max_col)]
        pin = _parse_one_row(row_vals, cols)
        if pin is not None:
            rows.append(pin)

    wb.close()
    print(f"parsed {len(rows)} pin rows from {xlsm_path.name}", file=sys.stderr)
    return rows


