def _collect_gpio_buckets(pins: list[PinRow]) -> tuple[dict, dict, dict]:
    """Bucket gpio pins by controller and direction (input/out-low/out-high)."""
    ctrl_input: dict[str, list[str]] = defaultdict(list)
    ctrl_outlow: dict[str, list[str]] = defaultdict(list)
    ctrl_outhigh: dict[str, list[str]] = defaultdict(list)
    for pin in pins:
        if pin.resolved_func != "gpio":
            continue
        parsed = _parse_gpio_bank_index(pin.dt_pin_name)
        if not parsed:
            continue
        bank, idx = parsed
        ctrl = _BANK_TO_CTRL.get(bank)
        if not ctrl:
            continue
        entry = f"\t\t\t{_CTRL_TO_MACRO[ctrl]}({bank}, {idx})"
        d = pin.pin_direction.strip().lower()
        iv = pin.gpio_init_val.strip().lower()
        if d == "input" or d in ("bidirectional", "bidir"):
            ctrl_input[ctrl].append(entry)
        elif d == "output":
            (ctrl_outhigh if iv == "1" else ctrl_outlow)[ctrl].append(entry)
    return ctrl_input, ctrl_outlow, ctrl_outhigh


def _emit_gpio_ctrl_block(ctrl: str, ctrl_input: dict, ctrl_outlow: dict,
                          ctrl_outhigh: dict) -> list[str]:
    """Emit DTSI lines for a single gpio controller's default block."""
    lines = [f"{ctrl} {{", "\t default {"]
    if ctrl_input.get(ctrl):
        lines.append("\t\tgpio-input = <")
        lines.extend(ctrl_input[ctrl])
        lines.append("\t\t\t>;")
    lines.append("\t\tgpio-output-low = <")
    lines.extend(ctrl_outlow.get(ctrl, []))
    lines.append("\t\t\t>;")
    lines.append("\t\tgpio-output-high = <")
    lines.extend(ctrl_outhigh.get(ctrl, []))
    lines.append("\t\t\t>;")
    lines.append("\t};")
    lines.append("};")
    return lines


def generate_gpio_dtsi(pins: list[PinRow], carrier_name: str,
                       xlsm_name: str) -> str:
    """Render the gpio default-state DTSI for the given parsed pins."""
    del carrier_name  # part of the public DTSI-generator signature
    ctrl_input, ctrl_outlow, ctrl_outhigh = _collect_gpio_buckets(pins)

    lines: list[str] = [
        f"/*This dtsi file was generated by generate_dtsi.py from {xlsm_name} */",
        _SPDX_HEADER,
        '#include "tegra264-gpio.h"',
        "",
    ]
    for ctrl in _CTRL_ORDER:
        if not (ctrl_input.get(ctrl) or ctrl_outlow.get(ctrl)
                or ctrl_outhigh.get(ctrl)):
            continue
        lines.extend(_emit_gpio_ctrl_block(
            ctrl, ctrl_input, ctrl_outlow, ctrl_outhigh))
    lines.append("")
    return "\n".join(lines)


