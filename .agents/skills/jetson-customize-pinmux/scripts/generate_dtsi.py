"""Generate pinmux / gpio / padvoltage DTSIs from a Jetson Thor pinmux XLSM.

Replicates the "Customer Generate DT" Excel macro on Linux without requiring
Excel or LibreOffice. Reads the XLSM with openpyxl (no VBA execution) and
emits three DTSI files that are byte-for-byte compatible with the macro output.

Usage::

    python3 generate_dtsi.py \\
        --xlsm  /path/to/Jetson_Thor_Series_Modules_Pinmux_Template_v1.7.xlsm \\
        --out-dir /path/to/Linux_for_Tegra/bootloader \\
        --carrier-name p3834-xxxx-p4071-0000   # suffix used in filenames

Output (all in --out-dir):
    tegra264-mb1-bct-pinmux-<carrier-name>.dtsi
    tegra264-mb1-bct-gpio-<carrier-name>.dtsi
    tegra264-mb1-bct-padvoltage-<carrier-name>.dtsi

Verified against Thor r38.4 devkit DTSIs generated by
T264_Pinmux_Config_CVM_Jedha_P4070.xlsm Revision 9.
"""

