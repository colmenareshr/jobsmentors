_DIR_TITLE_MAP = {
    "input": "Input", "output": "Output",
    "bidirectional": "Bidirectional", "bidir": "Bidirectional",
    "unused": "Unused",
}
_PULL_TITLE_MAP = {"none": "None", "pull-up": "Pull-Up", "pull-down": "Pull-Down"}
_DRV_TITLE_MAP = {"normal": "Disable", "high": "Enable"}


def _apply_direction_state(pin: PinRow, direction: str, init_state: str) -> None:
    """Update pin direction / enable-input / tristate from an edit."""
    if direction in _DIR_TITLE_MAP:
        pin.pin_direction = _DIR_TITLE_MAP[direction]
    if direction in ("input", "bidirectional"):
        pin.e_input = "Enable"
        pin.tristate = "Disable"
    elif direction == "output":
        pin.e_input = "Disable"
        pin.tristate = "Disable"
    elif direction == "unused":
        pin.e_input = "Disable"
        pin.tristate = "Enable"
    if init_state == "hi-z":
        pin.tristate = "Enable"


def _apply_electrical(pin: PinRow, edit: dict) -> None:
    """Apply pull / drv / open_drain / loopback overrides to a pin."""
    pull = (edit.get("pull") or "").lower()
    if pull in _PULL_TITLE_MAP:
        pin.pupd = _PULL_TITLE_MAP[pull]
    drv = (edit.get("drive_type") or "").lower()
    if drv in _DRV_TITLE_MAP:
        pin.drv_type = _DRV_TITLE_MAP[drv]
    od = (edit.get("open_drain") or "").lower()
    if od in ("enable", "disable"):
        pin.e_io_od = od.capitalize()
    lb = (edit.get("loopback") or "").lower()
    if lb in ("enable", "disable"):
        pin.e_lpbk = lb.capitalize()


def _apply_one_edit(pin: PinRow, edit: dict) -> PinRow:
    """Return a copy of `pin` with the user's edit baked in."""
    pin = copy.copy(pin)
    sfio = edit.get("sfio", pin.resolved_func)
    pin.resolved_func = sfio
    if sfio not in pin.sfio_options:
        pin.sfio_options = [sfio] + list(pin.sfio_options)
    direction = (edit.get("direction") or "").lower()
    init_state = (edit.get("initial_state") or "n/a").lower()
    if sfio == "gpio":
        pin.gpio_init_val = {"high": "1", "low": "0"}.get(init_state, "z")
    _apply_direction_state(pin, direction, init_state)
    _apply_electrical(pin, edit)
    return pin


def apply_pin_edits(pins: list[PinRow], edits: list[dict]) -> list[PinRow]:
    """Override PinRow fields with per-pin edits from session.pinmux.pin_edits."""
    edit_by_pin = {e["pin"]: e for e in edits}
    result: list[PinRow] = []
    for pin in pins:
        edit = edit_by_pin.get(pin.dt_pin_name)
        if edit:
            pin = _apply_one_edit(pin, edit)
        result.append(pin)
    return result


def build_pinmap_records(pins: list[PinRow]) -> list[dict]:
    """Convert PinRow list to pinmap-compatible dicts for modify_pinmux lookup.

    The sfio field uses the ``mode=name`` encoding that ``_sfio_options()``
    in modify_pinmux.py expects:
      - ``"gpio=gpio"``   → mode gpio  → emits ``"gpio"``
      - ``"sfio0=i2c7_clk"`` → mode sfio0 → emits ``"i2c7_clk"``

    The gpio bank/idx is NOT encoded in the sfio string here; modify_pinmux
    derives it from the DT pin name suffix (``_p<bank><idx>``), which is
    present in every Thor dt_pin_name produced by the XLSM.
    """
    records: list[dict] = []
    for pin in pins:
        # Build sfio list in mode=name format.
        sfio_entries: list[str] = []
        sfio_idx = 0
        for opt in pin.sfio_options:
            if opt == "gpio":
                sfio_entries.append("gpio=gpio")
            else:
                sfio_entries.append(f"sfio{sfio_idx}={opt}")
                sfio_idx += 1

        # Normalise direction to single-char convention used by existing
        # pinmap rows (I/O/B/U) so _print_row_details can display it.
        d = pin.pin_direction.strip().lower()
        dir_char = {"input": "I", "output": "O", "bidirectional": "B",
                    "bidir": "B", "unused": "U"}.get(d, d[:1].upper() if d else "")

        # Normalise XLSM raw strings to the values set-pin expects.
        pupd_raw = pin.pupd.strip().lower()
        pupd_norm = ("pull-up" if "up" in pupd_raw
                     else "pull-down" if "down" in pupd_raw
                     else "none")
        drv_norm = "high" if pin.drv_type.strip().lower() == "enable" else "normal"
        od_norm = "enable" if pin.e_io_od.strip().lower() == "enable" else "disable"
        lpbk_norm = "enable" if pin.e_lpbk.strip().lower() == "enable" else "disable"

        # A pad is software-configurable (pull/drv/od/lpbk editable) only when
        # its pad_type starts with "BD" or "BI" — i.e. BDGPIO*, BDDPAUX*,
        # BDSDMEM*, BIRST*, BICLK32K*, BIPOD*.  LP5XA_*, UPHYDS_*, DP_SINGLE_*,
        # BDMIPI16X_*, BDUSB2_*, OSCI27_* have empty attribute fields and are
        # fixed-function pads; per-pin questions 4-6 do not apply to them.
        configurable = pin.pad_type.upper().startswith(("BD", "BI"))

        records.append({
            "pin": pin.dt_pin_name,
            "ball": pin.package_ball,
            "connector_pin": pin.connector_pin,
            "verilog_name": pin.verilog_name,
            "signal_name": pin.signal_name,
            "customer_usage": pin.customer_usage,
            "direction": dir_char,
            "category": "other",
            "sfio": sfio_entries,
            "evidence": [],
            "power_rail": pin.power_rail,
            "pad_type": pin.pad_type,
            "por_state": pin.por_state,
            "configurable": configurable,
            "pupd": pupd_norm if configurable else "",
            "drv_type": drv_norm if configurable else "",
            "e_io_od": od_norm if configurable else "",
            "e_lpbk": lpbk_norm if configurable else "",
        })
    return records


def main(argv=None) -> int:
    """CLI entry point: parse the XLSM and write the 3 DTSI files."""
    p = argparse.ArgumentParser(
        description=__doc__,
        formatter_class=argparse.RawDescriptionHelpFormatter,
    )
    p.add_argument("--xlsm", required=True, type=pathlib.Path,
                   help="Path to the Jetson Thor pinmux template .xlsm")
    p.add_argument("--out-dir", required=True, type=pathlib.Path,
                   help="Output directory for the three .dtsi files")
    p.add_argument("--carrier-name", required=True,
                   help="Carrier suffix for filenames, e.g. p3834-xxxx-p4071-0000")
    p.add_argument("--dry-run", action="store_true",
                   help="Parse and report counts but do not write files")
    args = p.parse_args(argv)

    xlsm = args.xlsm.expanduser().resolve()
    if not xlsm.exists():
        print(f"error: xlsm not found: {xlsm}", file=sys.stderr)
        return 1

    out_dir = args.out_dir.expanduser().resolve()
    if not args.dry_run:
        out_dir.mkdir(parents=True, exist_ok=True)

    pins = parse_xlsm(xlsm)

    gpio_pins = [p for p in pins if p.resolved_func == "gpio"]
    sfio_pins = [p for p in pins if p.resolved_func != "gpio"]
    rails = {p.power_rail for p in pins if p.power_rail and p.io_voltage}

    print(f"  total pins : {len(pins)}", file=sys.stderr)
    print(f"  gpio pins  : {len(gpio_pins)}", file=sys.stderr)
    print(f"  sfio pins  : {len(sfio_pins)}", file=sys.stderr)
    print(f"  pad rails  : {len(rails)}  ({', '.join(sorted(rails))})", file=sys.stderr)

    if args.dry_run:
        print("dry-run: no files written", file=sys.stderr)
        return 0

    cn = args.carrier_name
    xlsm_name = xlsm.name

    files = {
        out_dir / f"tegra264-mb1-bct-pinmux-{cn}.dtsi":
            generate_pinmux_dtsi(pins, cn, xlsm_name),
        out_dir / f"tegra264-mb1-bct-gpio-{cn}.dtsi":
            generate_gpio_dtsi(pins, cn, xlsm_name),
        out_dir / f"tegra264-mb1-bct-padvoltage-{cn}.dtsi":
            generate_padvoltage_dtsi(pins, cn, xlsm_name),
    }

    for path, content in files.items():
        path.write_text(content, encoding="utf-8")
        print(f"wrote {path}", file=sys.stderr)

    return 0


if __name__ == "__main__":
    sys.exit(main())
