_MPIO_PAD_PREFIXES = ("BDGPIO", "BDSDMEM", "BDDPAUX")


def generate_pinmux_dtsi(pins: list[PinRow], carrier_name: str,
                         xlsm_name: str) -> str:
    """Render the pinmux DTSI for the given parsed pins and carrier name."""
    lines: list[str] = []
    lines.append(f"/*This dtsi file was generated by generate_dtsi.py from {xlsm_name} */")
    lines.append(_SPDX_HEADER)
    lines.append("")
    lines.append('#include "t264-pinctrl-tegra.h"')
    lines.append("")
    lines.append(f'#include "./tegra264-mb1-bct-gpio-{carrier_name}.dtsi"')
    lines.append("")
    lines.append("pinmux@ac281000 {")
    lines.append("\t\tcommon {")
    lines.append("\t\t\t/* SFIO Pin Configuration */")

    # Only emit MPIO pads (BDGPIO*, BDSDMEM*, BDDPAUX*) that have a pin_group
    # (col 32) — same filter as the Excel VBA macro.  Pins without a pin_group
    # are module-internal special pads (JTAG_TDO, PMU_INT_N, SOC_PWR_REQ,
    # SDMMC1_COMP) that the VBA macro does not emit.
    mpio_pins = [p for p in pins
                 if p.pad_type.startswith(_MPIO_PAD_PREFIXES) and p.pin_group]

    for pin in mpio_pins:
        is_sdmem = pin.pad_type.startswith("BDSDMEM")
        lines.append(f"\t\t\t{pin.dt_pin_name} {{")
        lines.append(f'\t\t\t\tnvidia,pins = "{pin.dt_pin_name}";')
        lines.append(f'\t\t\t\tnvidia,function = "{pin.resolved_func}";')
        lines.append(f"\t\t\t\tnvidia,pull = <{_pull_val(pin.pupd)}>;")
        lines.append(f"\t\t\t\tnvidia,tristate = <{_tristate_for_dir(pin)}>;")
        lines.append(f"\t\t\t\tnvidia,enable-input = <{_einput_for_dir(pin)}>;")
        lines.append(f"\t\t\t\tnvidia,drv-type = <{_drv_type_val(pin.drv_type)}>;")
        if not is_sdmem:
            # BDSDMEM pads have no e-io-od property in the reference DTSI
            lines.append(f"\t\t\t\tnvidia,e-io-od = <{_tegra_pin_val(pin.e_io_od)}>;")
        lines.append(f"\t\t\t\tnvidia,e-lpbk = <{_tegra_pin_val(pin.e_lpbk)}>;")
        lines.append("\t\t\t};")
        lines.append("")

    lines.append("\t\t};")
    lines.append("};")
    lines.append("")
    return "\n".join(lines)


